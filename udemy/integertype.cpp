#include<bits/stdc++.h>
using namespace std;
int main(int argc, char const *argv[]) {
  int value = -456495215 ;
  unsigned int uvalue = -548541 ;
  short int svalue = -4564 ;
  std::cout << value << '\n';
  std::cout << uvalue << '\n';
  std::cout << svalue << '\n';
  std::cout << "int max value: " << INT_MAX << '\n';
  std::cout << "int min value: " << INT_MIN << '\n';
  std::cout << "size of int variable value:  " << sizeof(int) << '\n';
  std::cout << "size of char variable value:  " << sizeof(char) << '\n';
  std::cout << "size of float variable value:  " << sizeof(float) << '\n';
  std::cout << "size of long variable value:  " << sizeof(long) << '\n';
  std::cout << "size of double variable value:  " << sizeof(double) << '\n';
  std::cout << "size of short int variable value:  " << sizeof(short int) << '\n';

  return 0;
}
